/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class BSTIterator {
public:
    BSTIterator(TreeNode* root) {
        cur_ = root;
        while (cur_) {
            st_.push(cur_);
            cur_ = cur_->left;
        }
    }
    
    /** @return the next smallest number */
    int next() {
        if (st_.empty()) return -1;
        int res = st_.top()->val;
        cur_ = st_.top()->right;
        st_.pop();
        while (cur_) {
            st_.push(cur_);
            cur_ = cur_->left;
        }
        return res;
    }
    
    /** @return whether we have a next smallest number */
    bool hasNext() {
        return !st_.empty();
    }
private:
    TreeNode *cur_;
    stack<TreeNode*> st_;
};

